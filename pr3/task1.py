def encode(dictionary):
    z1 = int(dictionary['z1']) & 0b00000000000_000000_00_000000_000000_111
    z2 = int(dictionary['z2']) << 3 & 0b00000000000_000000_00_000000_111111_000
    z3 = int(dictionary['z3']) << 9 & 0b00000000000_000000_00_111111_000000_000
    z5 = int(dictionary['z5']) << 17 & 0b00000000000_111111_00_000000_000000_000
    z6 = int(dictionary['z6']) << 23 & 0b11111111111_000000_00_000000_000000_000
    return hex(z6 | z5 | z3 | z2 | z1)


print(encode({'z1': '3', 'z2': '2', 'z3': '11', 'z5': '32', 'z6': '525'}))
