def encode(tupl):
    v1 = tupl[0] & 0b000000_0_0000_0_000000000_111111111
    v2 = tupl[1] << 9 & 0b000000_0_0000_0_111111111_000000000
    v3 = tupl[2] << 18 & 0b000000_0_0000_1_000000000_000000000
    v4 = tupl[3] << 19 & 0b000000_0_1111_0_000000000_000000000
    v5 = tupl[4] << 23 & 0b000000_1_0000_0_000000000_000000000
    v6 = tupl[5] << 24 & 0b111111_0_0000_0_000000000_000000000
    return v1 | v2 | v3 | v4 | v5 | v6


